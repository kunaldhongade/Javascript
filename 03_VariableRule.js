// rules for naming variables
/***
 * can't start with number
 * use underscore(_) or dollar symbol
 * * $variableName
 * * _variableName
 * can't use spaces in between
 * 
 * Convention always start with small letter & camelCase
 */

var a = 10;
var _a = 23;
var $a = 23;
var a1a = 45;
var pow = a ** 2;

console.log(a, _a, $a, a1a, pow);

// using underscore between two words are snake writing

var this_is_my_variable = 123;
console.log(this_is_my_variable);